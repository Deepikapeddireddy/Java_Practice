public class Loop{
    public static void main(String args[]){
        for(int i=1;i<5;i++)
        {
            System.out.println("For loop ");
        }
        int j=5;

        while(j!=0){
            System.out.println("While loop ");
            j--;
        }

        int  k=5;
        do{
            System.out.println("Do While loop ");
            k--;

        }while(k!=0);
    }
}