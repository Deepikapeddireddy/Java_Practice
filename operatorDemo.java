public class operatorDemo{
    public static void main(String args[]){
        int x = 10;
        float y=4.3f;
        float add = x+y;
        float sub = x-y;
        float mul = x*y;
        float div = x/y;
        float rem = x%y;

        System.out.println(sub);
        System.out.println(add);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(rem);

        int n = 5;
        System.out.println(n++);
        System.out.println(++n);

        System.out.println(n--);
        System.out.println(--n);
        
    }
}