public class demo{
    public static void main(String args[]){
          System.out.println("Hello world");
          int $_a =3;
          System.out.println($_a); // var declaration
          int num = 50_00_00_000; // 4 bytes
          System.out.println(num);//int values
          float f= 4.53f;// 4 bytes
          System.out.println(f); // float values
          double d =5.63;// 8 bytes
          System.out.println(d);// by default it will store double
          short s = 4243;// 2bytes
          System.out.println(s);
          byte b = 1;
          System.out.println(b);
          long l = 5000000000l;//8 bytes
          System.out.println(l);
          char c = 'A'; 
          char ch = 97;// 1 byte
          System.out.println(ch +" "+c);

          double d1 = 4; // implicit conversion
          System.out.println(d1);
          int k = (int)49.64;//type casting  
          System.out.println(k);

    
   
    }
}